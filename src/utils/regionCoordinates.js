// 지역명 정규화 함수
export const normalizeRegionName = (regionName) => {
  // 시도명 추출
  const sidoMap = {
    '서울특별시': '서울',
    '서울시': '서울',
    '부산광역시': '부산',
    '부산시': '부산',
    '대구광역시': '대구',
    '대구시': '대구',
    '인천광역시': '인천',
    '인천시': '인천',
    '광주광역시': '광주',
    '광주시': '광주',
    '대전광역시': '대전',
    '대전시': '대전',
    '울산광역시': '울산',
    '울산시': '울산',
    '세종특별자치시': '세종',
    '세종시': '세종',
    '경기도': '경기',
    '강원도': '강원',
    '강원특별자치도': '강원',
    '충청북도': '충북',
    '충북': '충북',
    '충청남도': '충남',
    '충남': '충남',
    '전라북도': '전북',
    '전북': '전북',
    '전북특별자치도': '전북',
    '전라남도': '전남',
    '전남': '전남',
    '경상북도': '경북',
    '경북': '경북',
    '경상남도': '경남',
    '경남': '경남',
    '제주특별자치도': '제주',
    '제주도': '제주',
    '제주': '제주'
  };
  
  // 직접 매핑 확인
  if (sidoMap[regionName]) {
    return sidoMap[regionName];
  }
  
  // 시군구가 포함된 경우 처리
  for (const [key, value] of Object.entries(sidoMap)) {
    if (regionName.includes(key) || regionName.includes(value)) {
      return value;
    }
  }
  
  // 시군구명만 있는 경우
  const cleanName = regionName.replace(/(시|군|구)$/g, '');
  if (regionCoordinates[cleanName]) {
    return cleanName;
  }
  
  return regionName;
};

// 좌표 가져오기 함수
export const getRegionCoordinates = (regionName) => {
  const normalizedName = normalizeRegionName(regionName);
  return regionCoordinates[normalizedName] || regionCoordinates['서울']; // 기본값
};